window.setLanguage = function (lang) {
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    if (translations[lang] && translations[lang][key]) {
      el.innerHTML = translations[lang][key];
    }
  });

  document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
    const key = el.getAttribute('data-i18n-placeholder');
    if (translations[lang] && translations[lang][key]) {
      el.placeholder = translations[lang][key];
    }
  });

  const btnId = document.getElementById('lang-id');
  const btnEn = document.getElementById('lang-en');

  if (btnId && btnEn) {
    if (lang === 'id') {
      btnId.className = 'text-primary font-bold cursor-pointer transition';
      btnEn.className = 'text-gray-500 hover:text-primary cursor-pointer transition';
    } else {
      btnEn.className = 'text-primary font-bold cursor-pointer transition';
      btnId.className = 'text-gray-500 hover:text-primary cursor-pointer transition';
    }
  }
};

